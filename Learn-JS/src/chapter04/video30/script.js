
//function
function greeting(a, b) {
    return a + b
}

console.log(greeting(6, 10))

// arrow function
const greeting1 = function (a1, b1) {
    return a1 + b1
}

console.log(greeting1(6, 10))


// arrow function expression
const greeting2 = (a2, b2) => {
    return a2 + b2
}

console.log(greeting2(6, 10))